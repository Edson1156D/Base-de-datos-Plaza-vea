    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        try {
            System.out.println("Creando objeto producto...");
            modelo.Producto p = new modelo.Producto();

            // 2. Validar campos vac√≠os
            if(NombreProducto.getText().isEmpty() || Stock.getText().isEmpty() || Precio.getText().isEmpty()){
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor llena todos los campos");
                return;
            }

            // 3. Llenar el objeto con los datos del formulario
            p.setNombre(NombreProducto.getText());
            p.setDescripcion(jTextArea1.getText());
            p.setIdTipo(Integer.parseInt(IDTipo.getText()));
            p.setStock(Integer.parseInt(Stock.getText()));
            p.setPrecio(Double.parseDouble(Precio.getText()));
            p.setIdProveedor(Integer.parseInt(txtIdProveedor1.getText()));// ¬°Aseg√∫rate que la caja de texto ID Proveedor se llama 'txtIdProveedor1' en tu dise√±o!
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
            java.util.Date fecha = sdf.parse(FechaCaducidad.getText());
            p.setFechaCaducidad(fecha);

            // 4. Guardar en Base de Datos
            controlador.ProductoController control = new controlador.ProductoController();

            // AQU√ç SE CREA LA VARIABLE EXITO
            boolean exito = control.guardarProducto(p);

            // 5. Verificar si funcion√≥
            if(exito){
                javax.swing.JOptionPane.showMessageDialog(this, "‚úÖ ¬°Producto Registrado!");

                // AQU√ç ES EL LUGAR CORRECTO PARA ACTUALIZAR LA TABLA
                llenarTabla();

                // Opcional: Limpiar las cajas de texto aqu√≠
                NombreProducto.setText("");
                Stock.setText("");
                Precio.setText("");

            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "‚ùå Error: La base de datos no guard√≥.");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "‚ùå Error de n√∫meros: Revisa Stock/Precio/IDTipo.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "‚ùå Error grave: " + e.getMessage());
            e.printStackTrace();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarActionPerformed

