    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String username = Usuario.getText().trim();
        String password = new String(Contrase침a.getPassword()).trim();

        // Validaci칩n de campos vac칤os
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Complete todos los campos.",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // Llamamos al controlador
        Usuario u = controller.iniciarSesion(username, password);

        // Validaci칩n del resultado
        if (u == null) {
            JOptionPane.showMessageDialog(
                    this,
                    "Credenciales incorrectas.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        } else {

            JOptionPane.showMessageDialog(
                    this,
                    "Bienvenido " + u.getUsername(),
                    "Acceso permitido",
                    JOptionPane.INFORMATION_MESSAGE
            );

            // Cerrar Login
            this.dispose();

            // Abrir Main
            MainForm main = new MainForm(u);
            main.setLocationRelativeTo(null); // Centrar ventana
            main.setVisible(true);
        }                                         
    }//GEN-LAST:event_btnIngresarActionPerformed

